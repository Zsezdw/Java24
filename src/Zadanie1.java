// Napisz program, który utworzy dwie zmienne, a następnie wypisze na ekran ich sumę, różnicę i iloczyn.

public class Zadanie1 {
    public static void main(String[] args) {

        int x;
        int y;
        int z;
        int a;
        int b;

        x = 6;
        y = 2;
        z = x + y;
        a = x - y;
        b = x*y;

        System.out.println("Suma liczb x+y wynosi:" + z);
        System.out.println("Różnica liczb x-y wynosi:" + a);
        System.out.println("Iloczyn liczb x*y wynosi:" + b);
    }
} // koniec klasy
